import { MonthlyFinancials, MONTHS_RU } from "@/types/finance";

function createMonthlyData(
  monthIndex: number,
  year: number,
  actual: {
    revenue: number;
    margin: number;
    marginPercent: number;
    profit: number;
    profitability: number;
  },
  budget: {
    revenue: number;
    margin: number;
    marginPercent: number;
    profit: number;
  },
  cashOnHand: number,
  projectsCount: number,
  isPast: boolean,
): MonthlyFinancials {
  const month = `${year}-${String(monthIndex + 1).padStart(2, "0")}`;
  return {
    month,
    label: MONTHS_RU[monthIndex],
    actualRevenue: actual.revenue,
    actualMargin: actual.margin,
    actualMarginPercent: actual.marginPercent,
    actualProfit: actual.profit,
    actualProfitability: actual.profitability,
    budgetRevenue: budget.revenue,
    budgetMargin: budget.margin,
    budgetMarginPercent: budget.marginPercent,
    budgetProfit: budget.profit,
    cashOnHand,
    projectsCount,
    isPast,
  };
}

